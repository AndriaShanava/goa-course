# ასევე რამდენი ხანით სურს დროშ მოგზაურობა შემდეგ კი ფუნქციამ უნდა დააბრუნოს დაბეჭდოს დროში მოგზაურობის შემდეგ რომელი
# წელი იქნება და რამდენი წლის იქნება მომხმარებელი,
# ასევე დაამატეთ რომ მომხმარებელმა მიიღოს გადაწყვეტილება წარსულში სურს დრში მოგზაურობა თუ მომავალში

def time_travel():
    # მოითხოვოს მომხმარებლის ასაკი და წლევანდელი წელი
    current_age = int(input("გთხოვთ, ჩაწეროთ თქვენი მიმდინარე ასაკი: "))
    current_year = int(input("გთხოვთ, ჩაწეროთ მიმდინარე წელი: "))
    
    # მოითხოვოს დროში მოგზაურობის ხანგრძლივობა
    travel_years = int(input("რამდენი წელი გსურთ დროში მოგზაურობა? "))
    
    # მოითხოვოს მომავალი თუ წარსული მიმართულების არჩევა
    direction = input("დარწმუნდით, რომ წარსულში (წ) თუ მომავალში (მ) მოგზაურობა გსურთ? (წ/მ): ")
    
    if direction == 'მ':
        future_year = current_year + travel_years
        future_age = current_age + travel_years
        print(f"მოგზაურობის შემდეგ იქნება წელი: {future_year} და თქვენ იქნებით: {future_age} წლის.")
    elif direction == 'წ':
        past_year = current_year - travel_years
        past_age = current_age - travel_years
        print(f"მოგზაურობის შემდეგ იქნება წელი: {past_year} და თქვენ იქნებით: {past_age} წლის.")
    else:
        print("არასწორი არჩევანი. გთხოვთ, აირჩიოთ 'წ' ან 'მ'.")

# ფუნქციის გაწვდვა
time_travel()
