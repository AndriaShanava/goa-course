# მომხმარებლის მიერ შემოტანილი რიცხვის აღება
number = float(input("შეიტანეთ რიცხვი: "))

# თუ რიცხვი დადებითია
if number > 0:
    # რიცხვის ატრიალება და უარყოფითი ნიშნით დაბრუნება
    result = -number
# თუ რიცხვი უარყოფითია
elif number < 0:
    # რიცხვის დადებითად გადაქცევა
    result = -number
else:
    # რიცხვი 0-ის შემთხვევაში
    result = 0

print(f"თქვენი რიცხვი: {result}")
